CREATE TABLE DEVELOPER.PERSONS
(
    PERSON_ID  NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL PRIMARY KEY,
    FIRST_NAME NUMBER                                                                NOT NULL,
    LAST_NAME  VARCHAR2(100)                                                         NOT NULL,
    CONSTRAINT UINT_ID CHECK ( PERSON_ID BETWEEN 0 AND 4294967295)
);

CREATE TABLE DEVELOPER.MEGATABLE
(
    NIGGER_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL PRIMARY KEY,
    SLAVE_NAME    VARCHAR2(100)                                                         NOT NULL,
    SLAVE_SURNAME VARCHAR2(100)                                                         NOT NULL,
    CONSTRAINT NIGGER_ID_UINT CHECK ( NIGGER_ID BETWEEN 0 AND 4294967295)
);

DROP TABLE DEVELOPER.PERSONS;
